package cn.itsource.springmvc.web.frontItem;

public class CompanyProduct {
	private String product_property;
	private String price;
	private String customer_income;
	private String customer_age;
	private String additional_information;
	private String ZIP_code;
	
	
	public String getProduct_property() {
		return product_property;
	}
	public void setProduct_property(String product_property) {
		this.product_property = product_property;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getCustomer_income() {
		return customer_income;
	}
	public void setCustomer_income(String customer_income) {
		this.customer_income = customer_income;
	}
	public String getCustomer_age() {
		return customer_age;
	}
	public void setCustomer_age(String customer_age) {
		this.customer_age = customer_age;
	}
	public String getAdditional_information() {
		return additional_information;
	}
	public void setAdditional_information(String additional_information) {
		this.additional_information = additional_information;
	}
	public String getZIP_code() {
		return ZIP_code;
	}
	public void setZIP_code(String zIP_code) {
		ZIP_code = zIP_code;
	}
	

}
